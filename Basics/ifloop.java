public class ifloop {
    public static void main(String[] args) {
        int a = 9;
        if (a == 10) {
            System.out.println("Jiff");
        }
        else if (a > 10) {
            System.out.println("Jaff");
        }
        else {
            System.out.println("Joff");
        }
        for (int i = 5; i < 5; i++) {
            System.out.println(i);
        }
    }    
}
 