package Tweak;

public class ObjectClass {
    public static void main(String[] args) {
        Object[] obj = new Object[10];
        obj[0] = 88;
        obj[1] = "Hello world";
        obj[2] = false;
        System.out.println(obj[0]);
        System.out.println(obj[1]);
        System.out.println(obj[2]);
    }
}
