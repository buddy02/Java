import java.util.Scanner;

public class datatypes {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int a = 10;
        float b = 10.2f;
        double c = 20.33;
        long d = 222l;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        input.close();
    }
    
}
